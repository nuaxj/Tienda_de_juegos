<h2>Dragon ball z:</h2>
</font>
</br>
</br>
<p align=center> <img src="imagenes\super.gif"> </img> </p >

<font face="Algerian">
<P ALIGN=center><h3>Los juegos son bastante diferentes de los Budokai, con los cuales se les compara a menudo. Los Tenkaichi utilizan una perspectiva de la cámara "detrás de la espalda" del personaje, viendo al personaje rival de frente con una ligera inclinación hacia un lado para evitar malas perspectivas que entorpezcan la visión del enemigo. A diferencia de los Budokai, cada forma de los personajes se trata como un personaje diferente, con diferentes características, set de movimientos y estilos, de forma similar a Dragon Ball Z: Super Sonic Warriors. Por su parte, el libre deambular es similar a Dragon Ball Z: Sagas . En batalla, los jugadores pueden cargar su barra de Ki para ejecutar diversas técnicas, tales como la Guardia fuerte, que reduce el daño al personaje por 1/4. La barra de Ki también puede utilizarse para el uso de habilidades llamadas Ráfaga 2. Cada personaje tiene un conjunto único de Ráfaga 2 que permiten el uso de movimientos especiales como ataques de Ki y ataques físicos. Los personajes también tienen una indicador numérico llamado Indicador de reserva que permite a los jugadores utilizar las técnicas llamadas Ráfaga 1. Las Ráfaga 1, por lo general, tienen un efecto de soporte que permite recuperar la salud, inmovilizar al enemigo o crear una barrera protectora. Los jugadores también pueden utilizar un Indicador de reserva para entrar en un modo conocido como Modo poder máximo, que hace que el personaje sea más rápido, más fuerte y capaz de utilizar movimientos exclusivos de la modalidad. Uno de estos movimientos es la Ráfaga definitiva, que suele ser el más poderoso movimiento que un personaje tiene. Sin embargo, el uso de la Ráfaga definitiva inmediatamente termina el Modo poder máximo.</p> </h3>
